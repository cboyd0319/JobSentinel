/**
 * Career Profile Selector for zero-config onboarding
 * Displays a grid of career paths for users to select from
 */

import { CAREER_PROFILES, type CareerProfile, type ProfileIconType } from "../utils/profiles";

interface CareerProfileSelectorProps {
  selectedProfile: string | null;
  onSelectProfile: (profileId: string | null) => void;
}

export function CareerProfileSelector({ selectedProfile, onSelectProfile }: CareerProfileSelectorProps) {
  return (
    <div className="animate-slide-up" role="radiogroup" aria-label="Select career profile">
      <div className="grid grid-cols-2 gap-3 mb-4">
        {CAREER_PROFILES.map((profile) => (
          <ProfileCard
            key={profile.id}
            profile={profile}
            isSelected={selectedProfile === profile.id}
            onSelect={() => onSelectProfile(profile.id)}
          />
        ))}
      </div>

      {/* Custom option */}
      <button
        onClick={() => onSelectProfile(null)}
        className={`
          w-full p-4 rounded-xl border-2 transition-all duration-200 text-left
          ${selectedProfile === null
            ? "border-surface-400 bg-surface-100"
            : "border-surface-200 hover:border-surface-300 bg-white"
          }
        `}
        role="radio"
        aria-checked={selectedProfile === null}
        aria-label="Custom Setup: I'll enter my own job titles and skills"
      >
        <div className="flex items-center gap-3">
          <div className={`
            w-10 h-10 rounded-lg flex items-center justify-center
            ${selectedProfile === null ? "bg-surface-500 text-white" : "bg-surface-100 text-surface-500"}
          `}>
            <SettingsIcon />
          </div>
          <div className="flex-1">
            <p className={`font-medium ${selectedProfile === null ? "text-surface-800" : "text-surface-700"}`}>
              Custom Setup
            </p>
            <p className="text-sm text-surface-500">I'll enter my own job titles and skills</p>
          </div>
        </div>
      </button>

      {/* Preview of selected profile */}
      {selectedProfile && (
        <SelectedProfilePreview profileId={selectedProfile} />
      )}
    </div>
  );
}

interface ProfileCardProps {
  profile: CareerProfile;
  isSelected: boolean;
  onSelect: () => void;
}

function ProfileCard({ profile, isSelected, onSelect }: ProfileCardProps) {
  return (
    <button
      onClick={onSelect}
      className={`
        p-4 rounded-xl border-2 transition-all duration-200 text-left
        ${isSelected
          ? "border-sentinel-500 bg-sentinel-50 ring-2 ring-sentinel-500/20"
          : "border-surface-200 hover:border-surface-300 bg-white hover:shadow-sm"
        }
      `}
      role="radio"
      aria-checked={isSelected}
      aria-label={`${profile.name}: ${profile.description}`}
    >
      <div className="flex flex-col gap-2">
        <div className={`
          w-10 h-10 rounded-lg flex items-center justify-center
          ${isSelected ? "bg-sentinel-500 text-white" : "bg-surface-100 text-surface-600"}
        `}>
          <ProfileIcon type={profile.icon as ProfileIconType} />
        </div>
        <div>
          <p className={`font-semibold ${isSelected ? "text-sentinel-700" : "text-surface-800"}`}>
            {profile.name}
          </p>
          <p className="text-xs text-surface-500 line-clamp-2">
            {profile.description}
          </p>
        </div>
        <div className="mt-1">
          <span className={`
            text-xs font-medium px-2 py-0.5 rounded-full
            ${isSelected ? "bg-sentinel-100 text-sentinel-700" : "bg-surface-100 text-surface-600"}
          `}>
            {profile.salaryRange}
          </span>
        </div>
      </div>
    </button>
  );
}

function SelectedProfilePreview({ profileId }: { profileId: string }) {
  const profile = CAREER_PROFILES.find(p => p.id === profileId);
  if (!profile) return null;

  return (
    <div className="mt-4 p-4 bg-sentinel-50 rounded-xl border border-sentinel-200" role="region" aria-labelledby="profile-preview-title" aria-live="polite">
      <p id="profile-preview-title" className="text-sm font-medium text-sentinel-800 mb-2">
        You'll see jobs like:
      </p>
      <div className="flex flex-wrap gap-2" role="list" aria-label="Sample job titles">
        {profile.sampleTitles.map((title) => (
          <span
            key={title}
            className="px-3 py-1 text-sm bg-white text-sentinel-700 rounded-full border border-sentinel-200"
            role="listitem"
          >
            {title}
          </span>
        ))}
        <span className="px-3 py-1 text-sm text-sentinel-600" aria-label={`Plus ${profile.titleAllowlist.length - profile.sampleTitles.length} more job titles`}>
          + {profile.titleAllowlist.length - profile.sampleTitles.length} more
        </span>
      </div>
      <p className="text-xs text-sentinel-600 mt-2">
        Pre-configured with {profile.keywordsBoost.length} relevant skills
      </p>
    </div>
  );
}

// Icon components for each profile type
function ProfileIcon({ type }: { type: ProfileIconType }) {
  switch (type) {
    case "code":
      return <CodeIcon />;
    case "shield":
      return <ShieldIcon />;
    case "chart":
      return <ChartIcon />;
    case "lightbulb":
      return <LightbulbIcon />;
    case "trending":
      return <TrendingIcon />;
    case "briefcase":
      return <BriefcaseIcon />;
    case "users":
      return <UsersIcon />;
    case "calculator":
      return <CalculatorIcon />;
    case "clipboard":
      return <ClipboardIcon />;
    case "pen":
      return <PenIcon />;
    case "heart":
      return <HeartIcon />;
    case "scale":
      return <ScaleIcon />;
    case "book":
      return <BookIcon />;
    case "smile":
      return <SmileIcon />;
    case "palette":
      return <PaletteIcon />;
    default:
      return <BriefcaseIcon />;
  }
}

